"""発言者の政治家/非政治家分類サービス.

役職のみの発言者名や参考人・証人など、政治家ではない発言者を
名前パターンに基づいて判定する。
"""

# 非政治家として扱う名前の完全一致パターン
# infrastructure層の TITLE_ONLY_PATTERNS をベースに、
# 議事録に頻出する非政治家カテゴリを網羅
NON_POLITICIAN_EXACT_NAMES: frozenset[str] = frozenset(
    {
        # 議会役職（個人を特定できない役職名のみの発言者）
        "委員長",
        "副委員長",
        "議長",
        "副議長",
        "仮議長",
        # 事務局スタッフ
        "事務局長",
        "事務局次長",
        "書記",
        "速記者",
        # 参考人・証人等
        "参考人",
        "証人",
        "公述人",
        # 政府側出席者（非議員）
        "説明員",
        "政府委員",
        "政府参考人",
        # 議会運営
        "幹事",
        # メタ情報（発言者ではないが発言者欄に出現することがある）
        "会議録情報",
    }
)


def is_non_politician_name(name: str) -> bool:
    """指定された名前が非政治家パターンに該当するかを判定する.

    Args:
        name: 発言者名

    Returns:
        非政治家パターンに該当する場合True
    """
    return name.strip() in NON_POLITICIAN_EXACT_NAMES
