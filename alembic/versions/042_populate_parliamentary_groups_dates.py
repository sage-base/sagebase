def upgrade() -> None:
    """parliamentary_groupsにstart_date/end_dateを設定し、is_activeを整合させる."""
    # 衆議院 歴史的会派: start_date + end_date を設定、is_active = false
    for name, start_date, end_date in SHUGIIN_HISTORICAL:
        _update_group(name, "衆議院", start_date, end_date, is_active=False)

    # 衆議院 現行会派: start_date のみ設定、is_active = true
    for name, start_date in SHUGIIN_ACTIVE:
        _update_group(name, "衆議院", start_date, None, is_active=True)

    # 参議院 現行会派: start_date のみ設定、is_active = true
    for name, start_date in SANGIIN_ACTIVE:
        _update_group(name, "参議院", start_date, None, is_active=True)


def downgrade() -> None:
    """start_date/end_dateをNULLに戻す（is_activeは元に戻せないため維持）."""
    op.execute("""
        UPDATE parliamentary_groups
        SET start_date = NULL, end_date = NULL
        WHERE chamber IN ('衆議院', '参議院')
    """)


def _update_group(
    name: str,
    chamber: str,
    start_date: str,
    end_date: str | None,
    *,
    is_active: bool,
) -> None:
    """会派のstart_date/end_date/is_activeを更新する."""
    escaped_name = name.replace("'", "''")
    if end_date is None:
        end_date_sql = "NULL"
    else:
        end_date_sql = f"'{end_date}'"

    is_active_sql = "TRUE" if is_active else "FALSE"

    op.execute(f"""
        UPDATE parliamentary_groups
        SET start_date = '{start_date}',
            end_date = {end_date_sql},
            is_active = {is_active_sql}
        WHERE name = '{escaped_name}'
          AND chamber = '{chamber}'
    """)
